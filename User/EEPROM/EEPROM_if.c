/***********************************************************************************************
*
* @brief    init the EEPROM module, this mudule is initialized after CTP init
* @param    none
* @return   none
*
************************************************************************************************/
void EEPROM_If_Init(void)
{
    EEPROM_Ctrl_Init();
}

/***********************************************************************************************
*
* @brief    send data to other module 
* @param  data_buf: the pointer of data
                data_size:  the size of data
                data_type: the type of storage data
* @return   none
*
************************************************************************************************/
void EEPROM_If_Data_Send(uint8_t *data_buf, uint8_t data_size, uint8_t data_type)
{
    switch(data_type)
    {
        case LCD_VCOM_ENUM:
                LCD_If_Eeprom_Send(data_buf, data_size);
            break;
        default :
            break;
    }
}

/***********************************************************************************************
*
* @brief    store data in eeprom 
* @param  data_buf: the pointer of data
                data_size:  the size of data
                data_type: the type of storage data
* @return   none
*
************************************************************************************************/
void EEPROM_If_Data_Save(uint8_t *data_buf, uint8_t data_size, uint8_t data_type)
{
    EEPROM_Ctrl_Save(data_buf, data_size, data_type);
}

/***********************************************************************************************
*
* @brief    read data in eeprom 
* @param  data_buf: the pointer of data
                data_size:  the size of data
                data_type: the type of read data
* @return   0: success
                1: error
*
************************************************************************************************/
uint8_t EEPROM_If_Data_Read(uint8_t *data_buf, uint8_t *data_size, uint8_t data_type)
{
    return EEPROM_Ctrl_Read(data_buf, data_size, data_type);
}

