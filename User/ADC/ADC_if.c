static uint8_t ADC_Init_Status=0; /* 0: not init, 1: the module has been initialized */

/**********************************************************************************************
* Local functions
**********************************************************************************************/


/**********************************************************************************************
* Global functions
**********************************************************************************************/

/***********************************************************************************************
*
* @brief    init ADC module
* @param    none
* @return   none
*
************************************************************************************************/
void ADC_If_Init(void)
{
    ADC_Ctrl_Init();
    ADC_Init_Status = 1;
}

/***********************************************************************************************
*
* @brief    deinit ADC module
* @param    none
* @return   none
*
************************************************************************************************/
void ADC_If_Deinit(void)
{
    ADC_Init_Status = 0;
    ADC_Ctrl_Deinit();
}
/***********************************************************************************************
*
* @brief    get ADC value according to the type
* @param    adc_type:  input voltage, PCB NTC, backlight NTC
* @return   uint16_t: the value of ADC
*
************************************************************************************************/
uint16_t ADC_If_Value_Get(ADC_IF_TYPE adc_type)
{
    if(ADC_Init_Status)
        return ADC_Ctrl_Value_Detect(adc_type);
    else
        return 0;
}

